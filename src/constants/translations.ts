export type TranslationKey = keyof typeof translations.en;

/** Simple template replacement: "Hello {name}" + {name: "World"} → "Hello World" */
export function t(
  locale: Locale,
  key: string,
  vars?: Record<string, string | number>,
): string {
  const table = translations[locale] ?? translations.en;
  let str = (table[key] ?? translations.en[key] ?? key) as string;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      str = str.replaceAll(`{${k}}`, String(v));
    }
  }
  return str;
}

/** Match UI locale to the browser language when possible */
export function detectLocale(): Locale {
  if (typeof navigator === "undefined") return "en";
  const lang = (navigator.language || "en").toLowerCase();
  const map: [string, Locale][] = [
    ["es", "es"], ["pt", "pt"], ["fr", "fr"], ["de", "de"], ["it", "it"],
    ["ja", "ja"], ["zh", "zh"], ["ko", "ko"], ["ru", "ru"], ["ar", "ar"],
    ["hi", "hi"], ["tr", "tr"], ["nl", "nl"], ["pl", "pl"],
  ];
  for (const [prefix, loc] of map) {
    if (lang.startsWith(prefix)) return loc;
  }
  return "en";
}
